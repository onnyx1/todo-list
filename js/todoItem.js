class todoItem {
  #taskName;
  #description;
  #dueDate;
  #projectLocation;
  #priority;

  constructor(taskName, description, dueDate, projectLocation, priority) {
    this.#taskName = taskName;
    this.#description = description;
    this.#dueDate = dueDate;
    this.#projectLocation = projectLocation;
    this.#priority = priority;
  }





  
}
